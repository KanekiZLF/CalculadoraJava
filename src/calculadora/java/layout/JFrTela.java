// Verifica se o ZERO n√£o esta sendo somado e faz a soma dos numeros 1 e 2.

private void verificaNum() {
num2 = Double.parseDouble(lValor.getText());
       if(num1 == 0 || num2 == 0){
       lValor.setText("Impossivel"); 
    } 
    else if ( null != opr)
            switch (opr) {
            case "+":
                result = num1 + num2;
                lValor.setText(Double.toString(result));
                break;
            case "-":
                result = num1 - num2;
                lValor.setText(Double.toString(result));
                break;
            case "*":
                result = num1 * num2;
                lValor.setText(Double.toString(result));
                break;
            case "/":
                result = num1/num2;
                lValor.setText(Double.toString(result));
                break;
            case "%":
                result = num1 % num2;
                lValor.setText(Double.toString(result));
                break;
            default:
                break;
        } 
}    
